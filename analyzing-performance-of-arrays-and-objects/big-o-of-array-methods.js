
// Section 3:16 - Big O of Array Methods

// Big O of Array Operations
// push - O(1) add to end
// pop - O(1) remove from end
// shift - O(N) add to beginning (reindex)
// unshift - O(N) remove from beginning (reindex)
// concat - O(N)
// slice - O(N)
// splice - O(N)
// sort - O(N * log N)
// forEach / map / filter / reduce / etc. - O(N)

// Limitations of Arrays
// Inserting at the beginning is not as easy as we might think!
//  There are more efficient data structures for that!