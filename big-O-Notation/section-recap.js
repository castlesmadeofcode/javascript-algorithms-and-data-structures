// Logarithm:
// The logarithm of a number roughly measures the number of
// times you can divide that number by 2 before you get a value that
// is less than or equal to one

//Big-O Recap:
// 1. To analyze the performance of an algorithm, we use Big O Notaiton
// 2. Big O Notation can give us a high level understanding of the time or
//  space complexity of an algorithm
// 3. Big O Notation doesn't care about precision, only about general
//  trends(linear/quadratic/constant)
// 4. The time or space complexity (as measured by Big O) depends only on
//  the algorithm. not the hardware used to run the algorithm
// 5. BIG O NOTATION IS EVERYWHERE, YOU CAN NOT ESCAPE IT.